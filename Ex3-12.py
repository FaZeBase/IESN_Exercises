"""
Cette fonction divise 2 nombres réels mis en paramètres et les retourne
Arguments : div(nb1: int, nb2: int) -> float
                ^           ^           ^
                |           |            - résultat
                |            - 2ème nombre à diviser
                 -1er nombre à diviser
"""
def div(nb1: int, nb2: int) -> float :
    return nb1 / nb2
print(div(4,2))
